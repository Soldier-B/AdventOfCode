function part2(map){
	return [[1, 1], [3, 1], [5, 1], [7, 1], [1, 2]]
		.map(rr => countTrees(map, rr[0], rr[1]))
		.reduce((a,b) => a * b);
}

function findTrees(map, run , rise){
	var height = map.length,
		width = map[0].length,
		x = 0,
		y = 0,
		trees = 0;

	while(y < height){
		if(map[y][x] == '#') trees++;
		x = (x + run) % width;
		y = y + rise;
	}

	return trees;
}

